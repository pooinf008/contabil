package br.edu.ifba.ads.inf008.exceptions;

public class ContabilException extends 
  Exception{

	public ContabilException() {
		super();
	}

	public ContabilException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ContabilException(String arg0) {
		super(arg0);
	}

	public ContabilException(Throwable arg0) {
		super(arg0);
	}
	
	

}
